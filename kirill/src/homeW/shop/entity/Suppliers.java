package entity;

public class Suppliers {
    private String contact_employee;
    private String company;
    private int cod_supplier;

    public String getContact_employee() {
        return contact_employee;
    }

    public void setContact_employee(String contact_employee) {
        this.contact_employee = contact_employee;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public int getCod_supplier() {
        return cod_supplier;
    }

    public void setCod_supplier(int cod_supplier) {
        this.cod_supplier = cod_supplier;
    }



}
